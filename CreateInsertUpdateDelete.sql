CREATE TABLE TEST 
(
  C1 NUMBER 
, C2 VARCHAR2(20) 
);


DECLARE
   COL1 TEST.C1%TYPE;
BEGIN
   COL1:=30;
   INSERT INTO TEST (C1,C2) VALUES (COL1,'XXXX');
   COMMIT;
END;
/

DECLARE
   T TEST.C1%TYPE;
BEGIN
   T:=10;
   UPDATE TEST SET C2='CCCCC' WHERE C1=T;
   COMMIT;
END;
/

DECLARE
    T TEST.C1%TYPE;
BEGIN
    T:=30;
    DELETE FROM TEST WHERE C1=T;
    COMMIT;
END;
/

